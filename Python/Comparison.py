# Comparing values

number1 = 10
number2 = 20

if number1 < number2: # Statement is true so it will run the code below
    print("number1 is greater than number2")
else: # What will happen if the statement is NOT true
    print("number1 is NOT greater number2")

# Comparison operators:
# == - Equal
# != - Not equal
# > - Greater than
# < - Less than
# > - Greater than or equal to
# < - Less than or equal to

# Comparison can be used with other data types than integers

first_name = "Timotej"
last_name = "Petrik"

if first_name == last_name:
    print("first_name and last_name are the same")
else:
    print("first_name and last_name are different")